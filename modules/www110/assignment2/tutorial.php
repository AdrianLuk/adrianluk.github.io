            <h4>Step Two: Navigating the Toolbar</h4>
            <p class="p-2">There are many buttons on the toolbar so I will be showing you the main ones that are not in your typical word processor.</p>
            <div class="d-block text-center"><a href="img/toolbar.png"><img src="img/toolbar.png" alt="toolbar snapshot"></a></div>
            <p class="p-1 text-center">A picture of the toolbar</p>
            <ol>
                <li><strong>Add Media</strong>: This button lets you add images, video, audio and other files into a library so that you can attach them to posts.</li>
                <li><strong>Insert/edit link</strong>: Transform any highlighted block of text into a hyperlink or edit an existing hyperlink.</li>
                <li><strong>Remove link</strong>: Removes a hyperlink from a selected block of text.</li>
                <li><strong>Insert Read More tag</strong>: Inserts a line dividing the content you want in an excerpt and the rest of the content. Anything above the line will be in an excerpt while the content below will be shown when the user clicks "read more".</li>
                <li><strong>Horizontal line</strong>: Inserts a horizontal line on the page. Used to divide sections of content for aesthetic purposes.</li>
                <li><strong>Paste as text</strong>: Paste any copied content as text without any external formatting. Usually used when copying pre-written content from word processors such as Microsoft Word to transfer only the text and not any additional styling from Word.</li>
                <li><strong>Special character</strong>: Insert a special character or symbol into your blog post.</li>
                <li><strong>Keyboard Shortcuts</strong>: Pressing this buttons brings up a small window with a list of keyboard shortcuts available.</li>
            </ol>

    </div>

    <div class="mb-3 border border-dark rounded p-4">

            <h4>Step Three: Adding Media</h4>
            <p class=" p-2">When you click the <b>Add Media</b> button, you will be brought to this screen:</p>
            <div class=" d-block text-center"><a href="img/media.png"><img class="img-fluid" src="img/media.png" alt="media screen snapshot"></a></div>
            <p class="p-1 text-center">The add media screen</p>
            <p class=" p-2">As you can see from the pictures above, the add media screen is very minimalistic. To add your media, simply drag and drop your file(s) into the window or click <b>Select Files</b> to browse for the file you want to upload. Your uploaded files can also be sorted and you can also create a photo gallery, audio playlist, video playlist and set the blog post's featured image. Additionally, you can also insert an image from a URL.</p>
            <p class=" p-2"><small><strong>*Remember to add alt tags when uploading images for accessibility and SEO purposes.</strong></small></p>

    </div>

